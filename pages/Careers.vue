  <script>
  // Optional JavaScript to trigger animations on scroll
  document.addEventListener('DOMContentLoaded', function() {
    // Intersection Observer for scroll animations
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('animated');
          observer.unobserve(entry.target);
        }
      });
    }, {
      threshold: 0.1
    });
  
    // Observe all elements with animate-on-scroll class
    document.querySelectorAll('.animate-on-scroll').forEach(element => {
      observer.observe(element);
    });
  
    // Add hover effect to job listings
    document.querySelectorAll('.bg-white.p-6').forEach(card => {
      card.addEventListener('mouseenter', function() {
        this.querySelector('svg').classList.add('animate-bounce');
      });
  
      card.addEventListener('mouseleave', function() {
        this.querySelector('svg').classList.remove('animate-bounce');
      });
    });
  });
  </script>
  